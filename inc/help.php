<div class="container impressum col-10">
    <h1>Help</h1>
    <p>
        Here you can find help to all the features of the KaraNatsch Social-Media page.
    </p>
    <br>
    <h3>Getting Started</h3>
    <p>You can create an account by clicking Log In and then "Register".
    Fill out all the necessary information and submit the form.</p>
    <p>Afterwards you can log yourself into your account and start having fun!</p>
    <h3>Impressum</h3>
    <p>
        Shows the legalities and contact informations of the KaraNatsch company.
    </p>

    <h3>Friends</h3>
    <p>
        Here you can view your list of friends, send and receive, as well as accept or decline friend-request.
        You can also cancel ongoing friendships.
    </p>
    <p>To add a Friend click "Add Friend" and request the User you want to be friends with. If they accept your request you can start chatting with them</p>
    <p>
        If you want to end an ongoing friendship, simply press the red user button with the x.
    </p>

    <h3>Chats</h3>
    <p>
        On this page, you can instant message with all your current friends.
        Select the Friend you want to Chat with and press the paper-plane button to start the Chat.
    </p>
    <p>
        Grey check marks indicate that the message has not been read yet by your friend. If the check mark turns green, it means your friend read the message.
    </p>

    <h3>Profile</h3>
    <p>
        The profile-page allows you to view your account details as well as changing them or even deleting it all!
    </p>
    <p>
        Press "Change Details" to edit your Data. After changing the Details press "Save Details" to save.
    </p>
    <p>
        In order to change your Password press "change password". You will then need to type your current password and then the new password twice to change the password.
    </p>


    <h3>Home</h3>
    <p>
        On the home-page, you can view all public posts and posts from your friends and even your own posts!
        With the click of a button a new post can be created! You can upload images or just write a simple text.
        You can also go through all your own posts and change who can view them or if you want to you can delete them entirely.
    </p>
    <p>You can Like, Dislike and even comment all Posts.</p>
    <p>If there are Comments for the post you can open them and if necessary you can even edit your comments. Just click <span style='color: #515151'<i class='fas fa-ellipsis-h'></i></span> to start editing or to delete your Comment.</p>